int	ft_isdigit(const char *s)
{
	if (!s || !*s)
		return (0);
	if ((*s == '-' || *s == '+') && !s[1])
		return (0);
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
		s++;
	return (*s == '\0');
}
